const char bin2hex_convtab[] = "0123456789abcdef";

unsigned char hex2bin(const char *str, unsigned char *bytes, size_t blen) {
   unsigned char idx0, idx1;
   size_t pos;

   for (pos = 0; ((pos < (blen*2)) && (pos < strlen(str))); pos += 2) {
      idx0 = (unsigned char)str[pos+0];
      idx1 = (unsigned char)str[pos+1];
      bytes[pos/2] = (unsigned char)(hex2bin_convtab[idx0] << 4) | hex2bin_convtab[idx1];
   }

   return pos / 2;
}

unsigned char bin2hex(const unsigned char *bytes, size_t blen, char *str, size_t slen) {
   size_t pos;

   memset(str, 0, slen);

   for (pos = 0; (pos < blen) && (pos < slen / 2); pos++) {
      str[pos * 2] = bin2hex_convtab[bytes[pos] >> 4];
      str[pos * 2 + 1] = bin2hex_convtab[bytes[pos] & 15];
   }

   return pos * 2;
}
